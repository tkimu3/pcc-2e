# Print the name using the three stripping functions
# strip(), lstrip(), rstrip()
first_name = " Steve "
last_name = " Jobs "
print(first_name)
print(last_name)
print(first_name.lstrip())
print(first_name.rstrip())
print(last_name.strip())
print(f"{first_name.strip()}\n")
print(f"\t{last_name.strip()}")